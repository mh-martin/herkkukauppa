        4. Oikeusperuste ja henkilötietojen käsittelyn tarkoitus<br>
        EU:n yleisen tietosuoja-asetuksen mukainen oikeusperuste henkilötietojen käsittelylle on asiakassuhde.
        Henkilötietojen käsittelyn tarkoitus on yhteydenpito asiakkaisiin, asiakassuhteen ylläpito sekä markkinointi.

        Tietoja ei käytetä automatisoituun päätöksentekoon tai profilointiin.


    </p>
    <p>

        5. Rekisterin tietosisältö<br>
        Rekisteriin tallennettavia tietoja ovat: henkilön nimi, yhteystiedot (puhelinnumero,
        sähköpostiosoite, osoite), sekä tehdyt tilaukset.
        Rekisteröidyt asiakkaat pysyvät rekisterissämme siihen asti, kunnes asiakas itse pyytää poistoa.
        Rekisteröitymättä tehtyjen tilausten asiakastiedot poistetaan puolen vuoden kuluttua tilauksesta.

    </p>
    <p>

        6. Säännönmukaiset tietolähteet<br>
        Rekisteriin tallennettavat tiedot saadaan asiakkaalta tilauksen yhteydessä. 

    </p>
    <p>

        7. Tietojen säännönmukaiset luovutukset ja tietojen siirto EU:n tai ETA:n ulkopuolelle<br>
        Tietoja ei luovuteta muille tahoille. Tietoja voidaan julkaista siltä osin kuin niin on
        sovittu asiakkaan kanssa.

    </p>
    <p>

        8. Rekisterin suojauksen periaatteet<br>
        Rekisterin käsittelyssä noudatetaan huolellisuutta ja tietojärjestelmien avulla käsiteltävät tiedot suojataan
        asianmukaisesti. Kun rekisteritietoja säilytetään Internet-palvelimilla, niiden laitteiston fyysisestä ja
        digitaalisesta tietoturvasta huolehditaan asiaankuuluvasti. Rekisterinpitäjä huolehtii siitä, että tallennettuja
        tietoja sekä palvelimien käyttöoikeuksia ja muita henkilötietojen turvallisuuden kannalta kriittisiä tietoja
        käsitellään luottamuksellisesti ja vain niiden työntekijöiden toimesta, joiden työnkuvaan se kuuluu.

    </p>
    <p>

        9. Tarkastusoikeus ja oikeus vaatia tiedon korjaamista<br>
        Jokaisella rekisterissä olevalla henkilöllä on oikeus tarkistaa rekisteriin tallennetut tietonsa ja vaatia
        mahdollisen virheellisen tiedon korjaamista tai puutteellisen tiedon täydentämistä. Mikäli henkilö haluaa
        tarkistaa hänestä tallennetut tiedot tai vaatia niihin oikaisua, pyyntö tulee lähettää kirjallisesti
        rekisterinpitäjälle. Rekisterinpitäjä voi pyytää tarvittaessa pyynnön esittäjää todistamaan henkilöllisyytensä.
        Rekisterinpitäjä vastaa asiakkaalle EU:n tietosuoja-asetuksessa säädetyssä ajassa (pääsääntöisesti kuukauden
        kuluessa).

    </p>
    <p>

        10. Muut henkilötietojen käsittelyyn liittyvät oikeudet<br>
        Rekisterissä olevalla henkilöllä on oikeus pyytää häntä koskevien henkilötietojen poistamiseen rekisteristä
        ("oikeus tulla unohdetuksi"). Niin ikään rekisteröidyillä on muut EU:n yleisen tietosuoja-asetuksen mukaiset
        oikeudet kuten henkilötietojen käsittelyn rajoittaminen tietyissä tilanteissa. Pyynnöt tulee lähettää
        kirjallisesti rekisterinpitäjälle. Rekisterinpitäjä voi pyytää tarvittaessa pyynnön esittäjää todistamaan
        henkilöllisyytensä. Rekisterinpitäjä vastaa asiakkaalle EU:n tietosuoja-asetuksessa säädetyssä ajassa
        (pääsääntöisesti kuukauden kuluessa).
    </p>


